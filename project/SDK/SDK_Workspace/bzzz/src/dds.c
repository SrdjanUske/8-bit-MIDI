u16 dds_freq_to_tunning_word(u32 freq_Hz, u32 sample_freq_Hz) {
	return (1 << 16)*freq_Hz/sample_freq_Hz;
}

s8 dds_next_sample(u16 tunning_word) {
	acc += tunning_word;
	return table[acc >> 8];
}




